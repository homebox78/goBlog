    tb.cols.forEach(function (co) { h += '<th class="' + co.cls + '">' + esc(co.label) + '</th>'; });
    h += '</tr></thead><tbody>';
    tb.rows.forEach(function (r) {
      h += '<tr class="' + (r.cls || "") + '">';
      r.cells.forEach(function (cell) { h += '<td class="' + cell.cls + '">' + esc(cell.v) + '</td>'; });
      h += '</tr>';
    });
    h += '</tbody></table></div></div>';
  });
  el.innerHTML = h;
}

